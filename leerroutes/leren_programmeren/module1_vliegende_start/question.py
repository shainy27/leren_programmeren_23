naam = str(input("Hallo wat is je naam? "))
leeftijd = int(input(f"Goedendag {naam}, hoe oud ben je? "))
eten = str(input(f"En als {leeftijd} jarige, wat eet je het liefst? "))
drinken = str(input(f"En wat drink je het liefst bij {eten}? "))

print(f"De {leeftijd} jarige {naam} drinkt het liefst {drinken} bij {eten}")